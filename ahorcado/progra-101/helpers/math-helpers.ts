export function addDosNumeros(num1: number, num2: number) {
    return num1 + num2;
}

export function addResta(num1: number, num2: number) {
    return num1 - num2;
}

 export function addMulti(num1: number, num2: number) {
    return num1 * num2;
}

 export function addDivide(num1: number, num2: number) {
    return num1 / num2;
}